/** Cambridge Cambridge Scale Score (CSS) — 80-230 mapped to raw % */
export function toCambridgeScaleScore(rawPercent: number, exam: CambridgeExam): number {
  // Cambridge uses a 80–230 scale.  Each exam maps its passing window differently.
  // Simplified linear projection: 0%=80, 100%=230.
  const css = Math.round(80 + (rawPercent / 100) * 150);
  const gradeB = CAMBRIDGE_GRADE_THRESHOLDS[exam].pass;
  const gradeA = CAMBRIDGE_GRADE_THRESHOLDS[exam].distinction;
  // Grade B boundary ≈ CSS 160 for B2 First / CAE / CPE
  // Scale is exam-specific; this is a reasonable approximation.
  return Math.max(80, Math.min(230, css));
}
