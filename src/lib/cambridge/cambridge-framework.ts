/** Get all tasks for a specific exam */
export function getTasksForExam(exam: CambridgeExam): CambridgeTask[] {
  return CAMBRIDGE_TASKS.filter((t) => t.exam === exam);
}

/** Get all tasks for a specific exam + skill */
export function getTasksForSkill(
  exam: CambridgeExam,
  skill: CambridgeSkill
): CambridgeTask[] {
  return CAMBRIDGE_TASKS.filter((t) => t.exam === exam && t.skill === skill);
}

/** Get task by its ID */
export function getTask(taskId: string): CambridgeTask | undefined {
  return CAMBRIDGE_TASKS.find((t) => t.id === taskId);
}

/** Map a Cambridge exam to its CEFR level */
export function examToCefr(exam: CambridgeExam): CefrLevel {
  return CAMBRIDGE_EXAM_META[exam].cefr;
}

/** Map a CEFR level to the relevant Cambridge exam(s) */
export function cefrToExams(cefr: CefrLevel): CambridgeExam[] {
  return Object.values(CAMBRIDGE_EXAM_META)
    .filter((m) => m.cefr === cefr)
    .map((m) => m.exam);
}

/** Get vocabulary topic list for an exam */
export function getTopicAreas(exam: CambridgeExam): string[] {
  return CAMBRIDGE_EXAM_META[exam].topicAreas;
}

/** Get grammar syllabus for an exam */
export function getGrammarSyllabus(exam: CambridgeExam): string[] {
  return CAMBRIDGE_EXAM_META[exam].grammarSyllabus;
}
