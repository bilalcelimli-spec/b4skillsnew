import type { CefrLevel } from "../assessment-engine/types";

// ─────────────────────────────────────────────────────────────────────────────
// 1. EXAM IDENTIFIERS
// ─────────────────────────────────────────────────────────────────────────────

export type CambridgeExam =
  | "STARTERS" // YLE Pre-A1
  | "MOVERS"   // YLE A1
  | "FLYERS"   // YLE A2
  | "KET"      // A2 Key (formerly Key English Test)
  | "PET";     // B1 Preliminary (formerly Preliminary English Test)

export type CambridgeSkill =
  | "READING_WRITING" // YLE combines reading & writing
  | "READING"
  | "WRITING"
  | "LISTENING"
  | "SPEAKING";

// ─────────────────────────────────────────────────────────────────────────────
// 2. TASK TYPE TAXONOMY
// ─────────────────────────────────────────────────────────────────────────────

export type CambridgeItemFormat =
  | "PICTURE_MATCHING"          // Match word/sentence to picture
  | "CATEGORISATION"            // Tick correct category / column
  | "SENTENCE_COMPLETION"       // Complete sentence from word bank
  | "CLOZE_MCQ"                 // Multiple-choice cloze (3–4 options)
  | "CLOZE_OPEN"                // Open cloze (no options given)
  | "WORD_FORM"                 // Word-formation cloze
  | "READING_MCQ"               // Read text, answer 3-option MCQ
  | "READING_GAPPED_TEXT"       // Sentences/paragraphs removed from text
  | "READING_MATCHING"          // Match people/headings to texts
  | "SHORT_MESSAGE"             // Write short email/message (~100 words)
  | "ARTICLE_OR_STORY"          // Write article/story (~100 words)
  | "DRAWING_LINES"             // Listening: match name to picture (YLE)
  | "PICTURE_DIALOGUE_MCQ"      // Listening: 3-picture MCQ
  | "GAP_FILL_LISTEN"           // Listening: complete notes/form
  | "MATCHING_LISTEN"           // Listening: match descriptions to options
  | "SPOT_DIFFERENCES"          // Speaking: find differences in two pictures
  | "DESCRIBE_PHOTO"            // Speaking: describe/talk about a photo
  | "COLLABORATIVE_TASK"        // Speaking: discuss and decide together
  | "PERSONAL_QUESTIONS"        // Speaking: answer questions about yourself
  | "STORY_SEQUENCING"          // Speaking YLE: sequence picture cards, tell story
  | "COLOURING_WRITING";        // Listening YLE: colour and write

export interface CambridgeTask {
  /** Unique identifier, e.g. "STARTERS_R_PART1" */
  id: string;
  exam: CambridgeExam;
  skill: CambridgeSkill;
  partNumber: number;
  name: string;
  format: CambridgeItemFormat;
  /** Number of scorable items in this part */
  itemCount: number;
  /** Marks per correct item */
  marksPerItem: number;
  /** Target CEFR level for items in this part */
  cefr: CefrLevel;
  /** Word count range for stimulus text (where applicable) */
  stimulusWordRange?: [number, number];
  /** Word count target for productive tasks (writing) */
  outputWordTarget?: [number, number];
  /** Number of answer options (MCQ only) */
  optionCount?: number;
  /** Description of what the candidate must do */
  candidateTask: string;
  /** Description of stimulus material */
  stimulusDescription: string;
  /** Distractor guidance for AI generation */
  distractorGuidance: string;
  /** Vocabulary / grammar constraints */
  languageConstraints: string;
}

